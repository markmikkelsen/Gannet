function F = EightGaussModel_noBaseline(x, freq)
% Function for eight-Gaussian model without a baseline

% x(1)     = Gaussian 1 amplitude
% x(2)     = Gaussian 1 width (1/(2*sigma^2))
% x(3)     = Gaussian 1 center freq
% x(4-6)   = Gaussian 2
% x(7-9)   = Gaussian 3
% x(10-12) = Gaussian 4
% x(13-15) = Gaussian 5
% x(16-18) = Gaussian 6
% x(19-21) = Gaussian 7
% x(22-24) = Gaussian 8

F = x(1) * exp(x(2) * (freq - x(3)).^2) + ...
    x(4) * exp(x(5) * (freq - x(6)).^2) + ...
    x(7) * exp(x(8) * (freq - x(9)).^2) + ...
    x(10) * exp(x(11) * (freq - x(12)).^2) + ...
    x(13) * exp(x(14) * (freq - x(15)).^2) + ...
    x(16) * exp(x(17) * (freq - x(18)).^2) + ...
    x(19) * exp(x(20) * (freq - x(21)).^2) + ...
    x(22) * exp(x(23) * (freq - x(24)).^2);
